<?php
declare(strict_types=1);

// 1) Composer autoload
require 'vendor/autoload.php';

// 2) Bootstrap app
require 'bootstrap.php';

// 3) envSetter
require_once UTILS_PATH . '/envSetter.util.php';

// Load dummy data
$users = require_once DUMMIES_PATH . '/users.staticData.php';
$planets = require_once DUMMIES_PATH . '/planets.staticData.php';

// â€”â€”â€” Connect to PostgreSQL â€”â€”â€”
$dsn = "pgsql:host={$pgConfig['host']};port={$pgConfig['port']};dbname={$pgConfig['db']}";
$pdo = new PDO($dsn, $pgConfig['user'], $pgConfig['pass'], [
  PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
]);

echo "âœ… Connected to PostgreSQL via PDO\n";

// â€”â€”â€” Seeding Users â€”â€”â€”
echo "ðŸŒ± Seeding usersâ€¦\n";

// Prepare the insert statement (UUID is auto-generated by DEFAULT in DB)
$stmt = $pdo->prepare("
  INSERT INTO public.\"users\" (
    fullname,
    password,
    contact_num,
    username,
    role,
    passport_img,
    flight_id
  ) VALUES (
    :fullname,
    :password,
    :contact_num,
    :username,
    :role,
    :passport_img,
    :flight_id
  )
");


foreach ($users as $u) {
  $stmt->execute([
    ':fullname' => $u['fullname'],
    ':password' => password_hash($u['password'], PASSWORD_DEFAULT),
    ':contact_num' => $u['contact_num'],
    ':username' => $u['username'],
    ':role' => $u['role'],
    ':passport_img' => $u['passport_img'],
    ':flight_id' => $u['flight_id'],
  ]);
}

$planetStmt = $pdo->prepare("
  INSERT INTO public.\"planets\" (
    name,
    distance_from_earth,
    planet_img
  ) VALUES (
    :name,
    :distance_from_earth,
    :planet_img
  )
");

foreach ($planets as $p) {
  $planetStmt->execute([
    ':name' => $p['name'],
    ':distance_from_earth' => $p['distance_from_earth'],
    ':planet_img' => $p['planet_img'], 
  ]);
}

echo "âœ… PostgreSQL seeding complete!\n";
