    ':ticket_code' => $b['ticket_code'],
    ':status' => $b['status'],
    ':feedback' => $b['feedback'],
  ]);
  $bookingId = $bookingStmt->fetchColumn();
}

$ticketStmt = $pdo->prepare("
  INSERT INTO public.\"tickets\" (
    flight_number,
    launch_pad,
    gate,
    qr_code
  ) VALUES (
    :flight_number,
    :launch_pad,
    :gate,
    :qr_code
  )
");

foreach ($tickets as $t) {
  $ticketStmt->execute([
    ':flight_number' => $t['flight_number'],
    ':launch_pad' => $t['launch_pad'],
    ':gate' => $t['gate'],
    ':qr_code' => $t['qr_code'],
  ]);
}

